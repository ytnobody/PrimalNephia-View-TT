package $appname;
use strict;
use warnings;
use Nephia;

our $VERSION = 0.02;

path '/' => sub {
    my $req = shift;
    return {
        template => 'index.tt',
        title => '$appname',
        envname  => config->{envname},
        apppath  => 'lib/' . __PACKAGE__ .'.pm',
    };
};

1;

