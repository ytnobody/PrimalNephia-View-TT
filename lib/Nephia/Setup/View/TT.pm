package Nephia::Setup::View::TT;
use strict;
use warnings;
use parent qw( Nephia::Setup::Base );
use File::Spec;

sub index_template_file {
    my $self = shift;
    my $body = $self->templates->{index_template_file};
    my $file = File::Spec->catfile($self->approot, qw/view index.tt/);
    $self->spew($file, $body);
}

1;

